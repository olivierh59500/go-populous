package populous

import "image/color"

var normalPalette = paletteFromAmiga12([32]uint16{
	0x000, 0x444, 0x666, 0x888, 0xaaa, 0xccc, 0x620, 0x840,
	0xa00, 0xa60, 0xaa0, 0x4a0, 0x280, 0x262, 0x248, 0x26c,
	0x000, 0x444, 0x666, 0x888, 0xaaa, 0xccc, 0x620, 0x840,
	0xa00, 0xa60, 0xaa0, 0x4a0, 0x280, 0x262, 0x248, 0x26c,
})

var worldsPalette = paletteFromAmiga12([32]uint16{
	0x000, 0x002, 0x023, 0x112, 0x011, 0x032, 0x044, 0x555,
	0x277, 0x777, 0x999, 0xaaa, 0xbb9, 0xcda, 0x999, 0xc77,
	0x000, 0x002, 0x023, 0x112, 0x011, 0x032, 0x044, 0x555,
	0x277, 0x777, 0x999, 0xaaa, 0xbb9, 0xcda, 0x999, 0xc77,
})

func Palette(index int, variant int) color.RGBA {
	if variant == 1 {
		return worldsPalette[index&31]
	}
	return normalPalette[index&31]
}

func paletteFromAmiga12(values [32]uint16) [32]color.RGBA {
	var out [32]color.RGBA
	for i, value := range values {
		r := uint8((value >> 8) & 0x0f)
		g := uint8((value >> 4) & 0x0f)
		b := uint8(value & 0x0f)
		out[i] = color.RGBA{R: r<<4 | r, G: g<<4 | g, B: b<<4 | b, A: 0xff}
	}
	return out
}
